  changeImage() {
    for (let i = 0; i < this.thumbnails.length; i++) {
      this.thumbnails[i].addEventListener('click', () => {
        let id = this.thumbnails[i].getAttribute('id');
        let img = this.figImage.querySelector('img');
        let a = this.figImage.querySelector('a');

        let ido = this.webs[id];

        // image
        img.setAttribute('src', `./images/${ido.image}.jpg`);
        img.setAttribute('alt', ido.title);

        a.setAttribute('href', `https://${ido.url}/`);
        a.setAttribute('title', ido.title);

        // text
        this.figText.innerHTML = ido.text;
        this.figText.style.backgroundColor = ido.color;
      });
    }
  }
